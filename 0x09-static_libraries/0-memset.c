/**
 * *_memset - fills memory with a constant byte
 * @b: memory area to be filled
 * @d: char to copy
 * @c: number of times to copy b
 *
 * Return: pointer to the memory area s
 */
char *_memset(char *b, char d, unsigned int c)
{
unsigned int i;
unsigned int n;

for (i = 0; i < n; i++)
{
b[i] = d;
}

return (b);
}
